export const GAME_TUTORIAL_SLUGS = Object.freeze(Object.keys(GAME_TUTORIALS));

export function resolveGameTutorialSlug(pathname) {
  const path = String(pathname || '').split('?')[0].toLowerCase();
  if (path === '/simulation' || path.startsWith('/simulation/') || path === '/eternalhunger' || path.startsWith('/eternalhunger/')) {
    return 'eternal-hunger';
  }
  if (path === '/twenty-questions' || path.startsWith('/twenty-questions/')) return 'twenty-questions';
  return GAME_TUTORIAL_SLUGS.find((slug) => path.split('/').includes(slug)) || '';
}

export function getGameTutorialForPath(pathname) {
  const slug = resolveGameTutorialSlug(pathname);
  return slug ? { slug, ...GAME_TUTORIALS[slug] } : null;
}
